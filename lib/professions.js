// Lookup helpers --------------------------------------------------------

// Returns the profession entry with the given id, or null.
//   findProfession('plumber') -> { id: 'plumber', he: 'אינסטלטור', ... }
export function findProfession(id) {
  if (!id) return null;
  return PROFESSIONS.find(p => p.id === id) || null;
}

// Returns an object { categoryKey: [profession, ...] } grouping by category.
// Useful for rendering the onboarding dropdown grouped by section.
export function professionsByCategory() {
  const out = {};
  for (const p of PROFESSIONS) {
    if (!out[p.category]) out[p.category] = [];
    out[p.category].push(p);
  }
  return out;
}

