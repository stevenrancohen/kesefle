// lib/professions.js
//
// Israeli profession catalog for Kesefle.
// When a new user signs up via WhatsApp they pick (or are inferred into) one
// of these professions; the result drives:
//   - lib/sheet-writer.js : which rows the dashboard generates
//                           (income_subs / expense_subs become row labels)
//   - bot/ExpenseBot_FIXED.gs : classifier weights (keywords_boost is added
//                               to the user's per-profession lexicon so
//                               "בטון" routes a contractor's expense correctly)
//   - api/admin/* + start.html : the "I am a..." dropdown (uses he/en)
//
// SCHEMA -------------------------------------------------------------------
//   id              lowercase ASCII, no spaces. Stable forever — used as the
//                   key in the user's row in Sheet1. Don't rename.
//   he              Hebrew display name as a person in that profession would
//                   actually say it (e.g. "רואה חשבון", not "מומחה בחשבונאות")
//   en              English fallback for non-Hebrew users / admin tooling
//   category        one of the 10 buckets:
//                     construction | professional_services | healthcare |
//                     tech | retail_service | creative | education |
//                     logistics | agriculture | employee
//   vat             typical VAT status in Israel:
//                     'osek_morshe'  - turnover usually >~120k ILS/yr
//                     'osek_patur'   - turnover usually <~120k ILS/yr
//                     'employee'     - שכיר, no VAT registration
//                     'employer'     - חברה בע"מ / מעסיק עם דוחות מורכבים
//                   (this is the COMMON case, not the only legal case —
//                    the bot lets the user override during onboarding)
//   income_subs     5–10 Hebrew subcategory strings to appear on this user's
//                   income side of the dashboard. The first is the default.
//   expense_subs    5–12 Hebrew subcategory strings for the expense side.
//                   These become row labels in Sheet1 — keep them short
//                   (≤25 chars) so they render nicely RTL.
//   keywords_boost  10–20 *diagnostic* keywords (Hebrew + a few English)
//                   that should bias the classifier toward this profession's
//                   subcategory list. Avoid generic words ("כסף", "תשלום").
//   template_extras (optional) feature flags consumed by sheet-writer:
//                     'cash_flow_per_project' - per-project P&L tab
//                     'open_invoices'         - חשבוניות פתוחות tracker
//                     'mileage_log'           - km log + tax-deduction calc
//                     'inventory'             - stock-on-hand tab
//                     'tip_jar'               - daily tips column
//                     'gear_depreciation'     - equipment depreciation schedule
//                     'recurring_clients'     - retainer/subscription tracker
//                     'shift_log'             - hourly shift breakdown
//                     'commission_split'      - commission tracking
//                     'multi_currency'        - USD/EUR alongside ILS
//
// ADDING A NEW ENTRY -------------------------------------------------------
//   1. Pick an id no other entry uses.
//   2. Use the schema above. If your profession spans multiple categories
//      (e.g. a doctor who also has a clinic), pick the one that matches
//      MOST of their cash-flow reality.
//   3. Run `node -e "import('./lib/professions.js').then(m=>console.log(m.PROFESSIONS.length))"`
//      to sanity-check.
//   4. The dropdown auto-updates from PROFESSIONS — no UI edits needed.

